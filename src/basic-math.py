# This script demonstrates basic arithmetic and comparison operations in Python.

# Basic arithmetic operations
print(3 + 2)
print("3 + 2")
print(5 - 1)
print(3 * 4)
print(10 / 5)
print(35 % 3)
print(2 ** 4)
print(3.0 - 1)

# Using f-strings to print the results of arithmetic operations
print(f"3 + 2 = {3 + 2}")
print(f"5 - 1 = {5 - 1}")
print(f"3 * 4 = {3 * 4}")
print(f"10 / 5 = {10 / 5}")
print(f"35 % 3 = {35 % 3}")
print(f"2 ** 4 = {2 ** 4}")

# Comparison operations
print(5 == 1)
print(5 != 1)
print(5 > 1)
print(5 >= 1)
print(5 < 1)
print(5 <= 1)

# Using f-strings to print the results of comparison operations
print(f"5 == 1: {5 == 1}")
print(f"5 != 1: {5 != 1}")
print(f"5 > 1: {5 > 1}")
print(f"5 >= 1: {5 >= 1}")
print(f"5 < 1: {5 < 1}")
print(f"5 <= 1: {5 <= 1}")
